package domain;

public class ServiceDTO {
	
	private int sno;
	private String bno;
	private int stno;
	private int ebno;
	private int weight;
	private int fno;
	
	
	public int getSno() {
		return sno;
	}
	public void setSno(int sno) {
		this.sno = sno;
	}
	public String getBno() {
		return bno;
	}
	public void setBno(String bno) {
		this.bno = bno;
	}
	public int getStno() {
		return stno;
	}
	public void setStno(int stno) {
		this.stno = stno;
	}
	public int getEbno() {
		return ebno;
	}
	public void setEbno(int ebno) {
		this.ebno = ebno;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	public int getFno() {
		return fno;
	}
	public void setFno(int fno) {
		this.fno = fno;
	}
	
	
	
	
	
	

}
