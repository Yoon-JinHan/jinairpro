package domain;

public class FoodDTO {

	
	
	// fields
	private int fno;	
	private int price;
	
	// getter, setter
	public int getFno() {
		return fno;
	}
	public void setFno(int fno) {
		this.fno = fno;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	
	
	

} // class
